def get_base_price(brand_model_text: str, year: int) -> int:
    text_lower = brand_model_text.lower().strip()
    if year >= 2020: year_range = "2020-2026"
    elif year >= 2015: year_range = "2015-2019"
    elif year >= 2010: year_range = "2010-2014"
    elif year >= 2005: year_range = "2005-2009"
    else: year_range = "до_2004"
    
    for model_key, prices in MODEL_PRICES.items():
        if model_key in text_lower: return prices.get(year_range, DEFAULT_BASE_PRICE)
    for brand_key, prices in BRAND_PRICES.items():
        if brand_key in text_lower: return prices.get(year_range, DEFAULT_BASE_PRICE)
    return DEFAULT_BASE_PRICE

def calculate_price(brand_model, year, mileage, condition):
    base_price = get_base_price(brand_model, int(year))
    mileage_coeff = max(0.8, 1.0 - (int(mileage) / 3000000))
    cond_map = {"cond_excellent": 1.0, "cond_good": 0.85, "cond_repair": 0.65}
    cond_coeff = cond_map.get(condition, 0.8)
    raw_price = base_price * mileage_coeff * cond_coeff
    min_p = int(raw_price * 0.9)
    max_p = int(raw_price * 1.1)
    return f"{min_p:,}".replace(",", " "), f"{max_p:,}".replace(",", " ")

